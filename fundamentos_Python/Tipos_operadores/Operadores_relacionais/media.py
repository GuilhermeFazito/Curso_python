# Some três números e divida o resultado por 3 (média).

numero1 = int(input("Escreva a primeira nota: ")) 
numero2 = int(input("Escreva a segunda nota: ")) 
numero3 = int(input("Escreva a terceira nota: ")) 

resultado = ((numero1 + numero2 + numero3)/ 3)

print(f"A media das suas notas é: {resultado}")